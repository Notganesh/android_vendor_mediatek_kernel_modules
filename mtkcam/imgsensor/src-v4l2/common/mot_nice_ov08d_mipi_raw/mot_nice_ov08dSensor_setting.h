#define read_cmos_sensor_8(subctx, reg) \
	({ \
		u8 __val = 0xff; \
		char pu_send_cmd[1] = {(char)(reg & 0xff)}; \
		ov08d_i2c_rd_a8_d8(subctx->i2c_client, \
